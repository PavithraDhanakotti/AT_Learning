package src_onlineclass.DEC23_LOOPandCONDITION;

public class Ex0088 {
    public static void main(String[] args) {

        int i =1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        for (int j=1; j<=10; j=j+2){
            System.out.println(j);
        }
    }
}
